    /// 将中文机场名称映射为 IATA 代码
    /// - Parameter airportName: 中文机场名称或城市名
    /// - Returns: IATA 代码，如果未找到则返回原始名称
    static func mappedCode(for airportName: String) -> String {
        let trimmed = airportName.trimmingCharacters(in: .whitespaces)
        return chineseToIATA[trimmed] ?? trimmed
    }
    
    /// 检查是否为已知机场
    /// - Parameter name: 机场名称
    /// - Returns: 是否为已知机场
    static func isKnownAirport(_ name: String) -> Bool {
        let trimmed = name.trimmingCharacters(in: .whitespaces)
        return chineseToIATA[trimmed] != nil
    }
